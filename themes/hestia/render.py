def render_swaynag(variant: str) -> str:
    # Typeless dialogs keep the quiet dark base with an accent line; the two
    # built-in types are FULL-ACCENT banners (2026-07 redesign — they read as
    # the waybar accent baseline grown into a bar; the earlier orange warning
    # dropped so every destructive confirm speaks the one accent language).
    # Buttons on the accent are inverse chips — accent_fg fill, accent text —
    # flat (border-size 0) with roomier padding/gaps, the "modern" available
    # in swaynag's flat-rectangle vocabulary (no corner radius exists).
    # Typeless keeps the sunken-well buttons on the dark base.
    r, a, e = vroles(variant), vansi(variant), vext(variant)
    def b(h):
        return h.lstrip("#")
    # Since 0.10.0 the destructive-confirm banners (warning/error) speak DANGER
    # red, not the brand accent — the accent went violet, but "reboot? / exit?"
    # is a careful-now prompt, so it stays red. The typeless dialog keeps the
    # quiet accent (violet) line — it's decorative, not a loud confirm.
    danger_chip = (r["accent_fg"], r["danger"])
    sunken_well = (e["sunken"], r["text"])
    sections = [
        ("global / default (dialogs with no -t): dark base, accent line",
         None, r["bg"], r["text"], r["border"], r["accent"], sunken_well),
        ("warning (the sway exit confirm + power-menu confirms pass -t warning):",
         "warning", r["danger"], r["accent_fg"], r["danger_dark"], r["danger_dark"], danger_chip),
        ("error: same danger banner — one language for everything loud",
         "error", r["danger"], r["accent_fg"], r["danger_dark"], r["danger_dark"], danger_chip),
    ]
    out = [f"# {PROVENANCE}",
           f"# swaynag — hestia dialogs, {variant} (config-dark / config-light pair; the",
           "# bootstrap symlinks the `theme_variant` one to ~/.config/swaynag/config).",
           "# Values are bare RRGGBB (no '#'); annotations on their own lines.",
           "#",
           "# GOTCHA: swaynag's built-in `warning`/`error` types ship hardcoded colours and",
           "# do NOT layer the global options — only keys set INSIDE a type's [section]",
           "# override its presets, so the theme is restated in full per section.",
           ""]
    for title, section, bg, text, border, accent_line, (btn_bg, btn_fg) in sections:
        out.append(f"# --- {title} ---")
        if section:
            out.append(f"[{section}]")
        else:
            out += [
                "font=Lilex Nerd Font Mono 11",
                "# flat, roomy buttons (geometry is global-only — set before any [section])",
                "button-border-size=0",
                # button-padding matches message-padding on purpose: swaynag draws
                # buttons top-pinned (not vertically centred) and sizes the bar from the
                # message, so a smaller button-padding leaves the extra height as a gap
                # *below* the buttons (top-heavy). Equal paddings make the buttons fill
                # the bar height → symmetric top/bottom. (No top-padding key exists.)
                "button-padding=12",
                "button-gap=10",
                "button-dismiss-gap=10",
                "button-margin-right=6",
                "message-padding=12",
            ]
        out += [
            f"background={b(bg)}",
            f"border={b(border)}",
            f"border-bottom={b(accent_line)}",
            "border-bottom-size=2",
            f"text={b(text)}",
            f"button-background={b(btn_bg)}",
            f"button-text={b(btn_fg)}",
            f"details-background={b(e['sunken'])}",
            "",
        ]
    return "\n".join(out)


