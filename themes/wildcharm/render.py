        f"CAPABILITY {c('broken', bold=True)}",
        f"STICKY {c('dir', bold=True)}",
        f"OTHER_WRITABLE {c('dir')}",
        f"STICKY_OTHER_WRITABLE {c('dir')}",
        "",
        "# --- extension layer (restrained, wildcharm hues) ------------------------------",
