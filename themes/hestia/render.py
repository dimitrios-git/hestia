def render_sway(variant: str) -> str:
    r, a = vroles(variant), vansi(variant)
    # Focused-tile border (client.focused in user/sway/config) — cyan, not the
    # accent, so "this tile has focus" reads apart from the red accent surfaces.
    # Both variants ride ANSI 14 (bright_cyan): each table's own step (dark
    # #00d7d7, light #00afaf). Light was cyan-6 (#008787) at first for ground
    # contrast, but the brighter step won the live A/B — a 2px border needs
    # less contrast than text.
    focus = a["bright_cyan"]
    scheme = "prefer-dark" if variant == "dark" else "prefer-light"
    gtk_theme = "hestia-dark" if variant == "dark" else "hestia"
    # KDE Breeze cursors (breeze-cursor-theme, apt): the WHITE cursor on the
    # dark desktop, the standard dark "Breeze" on light. Names are the theme
    # DIRS in /usr/share/icons (index.theme: Breeze Light / Breeze).
    cursor = "Breeze_Light" if variant == "dark" else "breeze_cursors"
    return f"""# {PROVENANCE}
# sway theme fragment — {variant} (see config: include ~/.config/sway/theme.conf)

# Colors (roles from palette.yml)
set $bg        {r["bg"]}
set $surface   {r["surface"]}
set $border    {r["border"]}
set $text      {r["text"]}
set $dim       {r["dim"]}
set $accent    {r["accent"]}
set $accent_fg {r["accent_fg"]}
set $focus     {focus}

# Desktop background — the solid ground is the base and the fallback; the
# plain-mesh wallpaper (below) layers over it when its assets are installed.
output * bg {r["bg"]} solid_color

# Default wallpaper — per-output static mesh PNGs via wpaperd (the wallpaper
# verdict; the mpvpaper video loop was dropped for an unfixed upstream mpv
# memory leak — see wallpaper.sh; assets from the `wallpapers` role).
# exec_always is load-bearing: a reload respawns swaybg ABOVE the wallpaper
# layer, and the re-run re-kills it. No-ops (solid ground stays) when
# wpaperd/assets are absent.
exec_always $HOME/.config/sway/scripts/wallpaper.sh {variant}

# Mouse cursor — sway's own cursor (window drags, resize, the bare desktop).
# Clients draw their own: GTK reads the gsettings exec below + settings.ini,
# XWayland/Qt read start-sway's XCURSOR_THEME export (all follow the variant).
seat * xcursor_theme {cursor} 24

# GTK / portal appearance for this variant (CLAUDE.md GTK section): color-scheme
# is what GTK4/libadwaita + Firefox + portal-aware apps follow; gtk-theme is what
# GTK3 apps (and the portal file chooser) follow — GTK_THEME is deliberately unset
# (it broke GTK4 dark mode), so gsettings is the single GTK theme source now.
# `exec` runs at login only — not re-run on `swaymsg reload`.
exec gsettings set org.gnome.desktop.interface color-scheme {scheme}
exec gsettings set org.gnome.desktop.interface gtk-theme {gtk_theme}
exec gsettings set org.gnome.desktop.interface cursor-theme {cursor}
# Icon theme: the #7c3aed-accent Yaru built by the opt-in yaru_icons role
# (Yaru-hestia). Guard on the theme actually existing so a spin without that
# role doesn't force a missing theme.
exec sh -c 'test -d "$HOME/.local/share/icons/Yaru-hestia" && gsettings set org.gnome.desktop.interface icon-theme Yaru-hestia'
"""


def render_waybar_css(variant: str) -> str:
    r, a = vroles(variant), vansi(variant)
    tokens = {
        "bg": r["bg"],
        "text": r["text"],
        "muted": r["muted"],
        "border": r["border"],
        "surface_alt": r["surface_alt"],
        "accent": r["accent"],
        "accent_fg": r["accent_fg"],
        # per-widget signature accents (style.css): the power ⏻ is DANGER red
        # (power-off is a destructive action) and the focused-workspace marker is
        # the CYAN that matches sway's window-focus border ($focus == ansi
        # .bright_cyan). The launcher stays the violet accent (@accent).
        "danger": r["danger"],
        "focus": a["bright_cyan"],
        # muted-state icons (pulseaudio.muted) — ANSI bright_black, the
        # quietest grey that still reads on the bar
        "icon_muted": a["bright_black"],
    }
    lines = [f"/* {PROVENANCE}",
             f"   waybar colour tokens — {variant} (style.css: @import \"theme.css\") */", ""]
    lines += [f"@define-color {k} {v};" for k, v in tokens.items()]
    lines.append("")
    return "\n".join(lines)


# ----------------------------------------------- xterm-256 index (vifm cterm)

_CUBE = [0, 95, 135, 175, 215, 255]


def xterm_index(hexval: str) -> int:
    """Nearest xterm-256 index (16-255). Palette values are xterm-native except
    the two grounds (#1a1a1a -> 234, #f5f5f5 -> 255) — nearest steps, matching
    the cterm fallbacks hestia.vim uses."""
    h = hexval.lstrip("#")
    r, g, b = (int(h[i : i + 2], 16) for i in (0, 2, 4))
    best, best_d = 16, 3 * 255 ** 2 + 1
    for ri, rv in enumerate(_CUBE):
        for gi, gv in enumerate(_CUBE):
            for bi, bv in enumerate(_CUBE):
                d = (r - rv) ** 2 + (g - gv) ** 2 + (b - bv) ** 2
                if d < best_d:
                    best, best_d = 16 + 36 * ri + 6 * gi + bi, d
    for i in range(24):
        v = 8 + 10 * i
        d = (r - v) ** 2 + (g - v) ** 2 + (b - v) ** 2
        if d < best_d:
            best, best_d = 232 + i, d
    return best


