# per-variant role/ANSI tables for the desktop-chrome emitters
def vroles(variant: str) -> dict:
    return ROLES if variant == "dark" else LIGHT["roles"]


def vansi(variant: str) -> dict:
    return ANSI if variant == "dark" else LIGHT["ansi"]

FILL_ROLES = {"error", "todo"}  # reverse/fill — exempt from the plain-text gate


def _lum(hexval: str) -> float:
    h = hexval.lstrip("#")
    c = [int(h[i : i + 2], 16) / 255 for i in (0, 2, 4)]
    c = [x / 12.92 if x <= 0.04045 else ((x + 0.055) / 1.055) ** 2.4 for x in c]
    return 0.2126 * c[0] + 0.7152 * c[1] + 0.0722 * c[2]


def contrast(a: str, b: str) -> float:
    la, lb = sorted((_lum(a), _lum(b)), reverse=True)
    return (la + 0.05) / (lb + 0.05)


def aa_gate() -> None:
    failures = []
    for variant, colors in VARIANTS.items():
        for role, hexval in colors.items():
            if role.startswith("_") or role in FILL_ROLES or role.startswith("error"):
                continue
            for surface in colors["_aa_surfaces"]:
                r = contrast(hexval, surface)
                if r < 4.5:
                    failures.append(f"{variant}.{role} {hexval} on {surface}: {r:.2f}:1")
    if failures:
        sys.exit("AA contrast gate FAILED:\n  " + "\n  ".join(failures))


def token_colors(colors: dict) -> list:
    out = []
    for rule in SCOPES:
        settings = {}
        role = rule.get("role")
        if rule.get("paired"):
            settings["foreground"] = colors["error_fg"]
            settings["background"] = colors["error_bg"]
        elif role:
            settings["foreground"] = colors[role]
        # `is not None` so font_style: "" is emitted as fontStyle:"" — an
        # explicit CLEAR (TextMate resolves fontStyle independently of
        # foreground by specificity, so a bare fg rule would otherwise inherit
        # italic/bold from a less-specific scope; see the keys/property rule).
        if rule.get("font_style") is not None:
            settings["fontStyle"] = rule["font_style"]
        out.append({"name": rule["name"], "scope": rule["scopes"], "settings": settings})
    return out


# ---------------------------------------------------------------- tmTheme (bat)

def render_tmtheme(variant: str = "dark") -> str:
    c = VARIANTS[variant]
    r = vroles(variant)
    chrome = {
        "background": c["_canvas"],
        "foreground": c["text"],
        "caret": r["accent"],
        "lineHighlight": r["surface_alt"],
        "selection": r["border"],
        "invisibles": r["dim"],
        "guide": r["border"],
        "activeGuide": r["dim"],
        "gutterForeground": r["dim"],
    }
    lines = [
        '<?xml version="1.0" encoding="UTF-8"?>',
        '<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">',
        f"<!-- {PROVENANCE} -->",
        '<plist version="1.0">',
        "<dict>",
        "  <key>name</key>",
        "  <string>hestia</string>",
        "  <key>settings</key>",
        "  <array>",
        "",
        "    <dict>",
        "      <key>settings</key>",
        "      <dict>",
    ]
    for k, v in chrome.items():
        lines.append(f"        <key>{k}</key> <string>{v}</string>")
    lines += ["      </dict>", "    </dict>"]
    for tc in token_colors(c):
        lines += [
            "",
            "    <dict>",
            f"      <key>name</key><string>{tc['name']}</string>",
            f"      <key>scope</key><string>{', '.join(tc['scope'])}</string>",
            "      <key>settings</key>",
            "      <dict>",
        ]
        for k, v in tc["settings"].items():
            lines.append(f"        <key>{k}</key><string>{v}</string>")
        lines += ["      </dict>", "    </dict>"]
    lines += ["", "  </array>", "</dict>", "</plist>", ""]
    return "\n".join(lines)


